function isVowel(character) {
  if (character.toLowerCase() === 'a'.toLowerCase() || character.toLowerCase() === 'e'.toLowerCase() || character.toLowerCase() === 'i'.toLowerCase() || character.toLowerCase() === 'o'.toLowerCase() || character.toLowerCase() === 'u'.toLowerCase()) {
    return true;
  }
  return false;
}
